package constructorconcept;

public class customers {
	String name;
	int custID;
	double salary;
	String city;
	long phoneNumber;
	
	public customers(String name, int custID, double salary, String city, long phoneNumber) {
		super();
		this.name = name;
		this.custID = custID;
		this.salary = salary;
		this.city = city;
		this.phoneNumber = phoneNumber;
	}


}
